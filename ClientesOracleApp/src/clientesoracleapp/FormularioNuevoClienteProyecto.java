    private void guardarDatos() {
        String idCliente = txtIDCliente.getText();
        String dni = txtDNI.getText();
        String nombreCliente = txtNombreCliente.getText();
        String idProyecto = txtIDProyecto.getText();
        String nombreProyecto = txtNombreProyecto.getText();
        String fechaContrato = txtFechaContrato.getText();
        String detalleContrato = txtDetalleContrato.getText();

        try (Connection conn = DriverManager.getConnection(
                "jdbc:oracle:thin:@localhost:1521:xe", "SYSTEM", "Oracle")) {
            String sql = "INSERT INTO CLIENTE_PROYECTO (ID_CLIENTE, DNI, NOMBRE_CLIENTE, ID_PROYECTO, NOMBRE_PROYECTO, FECHA_CONTRATO, DETALLE_CONTRATO) " +
                         "VALUES (?, ?, ?, ?, ?, TO_DATE(?, 'YYYY-MM-DD'), ?)";
            PreparedStatement stmt = conn.prepareStatement(sql);
            stmt.setString(1, idCliente);
            stmt.setString(2, dni);
            stmt.setString(3, nombreCliente);
            stmt.setString(4, idProyecto);
            stmt.setString(5, nombreProyecto);
            stmt.setString(6, fechaContrato);
            stmt.setString(7, detalleContrato);

            int filasInsertadas = stmt.executeUpdate();
            if (filasInsertadas > 0) {
                JOptionPane.showMessageDialog(this, "Registro guardado correctamente.");
                dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Error al guardar los datos.");
            }

        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error en la base de datos: " + ex.getMessage());
        }
    }
